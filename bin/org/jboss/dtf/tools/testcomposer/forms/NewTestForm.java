    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        if (numberOfNamesEdit.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this, "You MUST specify the number of names required for this test", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            if (nameEdit.getText().length() == 0)
            {
                JOptionPane.showMessageDialog(this, "You MUST specify a name for this test", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                closeDialog(null);
            }
        }
    }//GEN-LAST:event_createButtonActionPerformed

    public TestDefinition getTestDefinition(String group)
    {
        return(new TestDefinition( group,
								   nameEdit.getText(),
								   descriptiveEdit.getText(),
				                   descriptionText.getText(),
				                   Integer.parseInt(numberOfNamesEdit.getText()),
								   new Hashtable()));
    }

